# Function to calculate the sum of a list of integers
def Calculate__Sum__Of__Integers(numbers):
    """
    Calculate the sum of a list of integers.

    Args:
        numbers (list): A list of integers.

    Returns:
        int: The sum of the integers in the list.
    """
    return sum(numbers)

# Example usage
numbers = [1, 2, 3, 4, 5]
print("Sum of integers:", Calculate__Sum__Of__Integers(numbers))