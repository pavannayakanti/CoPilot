""" Function for multiplying squares of two integers """
def Multiply__Squares(a, b):
    """
    This function multiplies the squares of two integers.
    
    Parameters:
    a (int): The first integer.
    b (int): The second integer.
    
    Returns:
    int: The product of the squares of a and b.
    """
    return a**2 * b**2
